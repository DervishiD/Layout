package llayout1.interfaces

/**
 * The interface representing the objects that have a dimension.
 * @since LLayout 1
 */
interface HavingDimension {

    /**
     * Returns a width.
     * @since LLayout 1
     */
    fun width() : Int

    /**
     * Returns a height.
     * @since LLayout 1
     */
    fun height() : Int

}